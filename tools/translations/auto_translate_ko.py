# Pattern-based fallback rules. Applied in order; each rule is (regex, lambda)
# Lambdas receive the regex match and return the translation.
PATTERN_RULES: list[tuple[re.Pattern[str], Callable[[re.Match[str]], str]]] = []


# -----------------------------------------------------------------------------

MESSAGE_RE = re.compile(r"<message\b[^>]*>(.*?)</message>", re.DOTALL)
SOURCE_RE = re.compile(r"<source>(.*?)</source>", re.DOTALL)
UNFINISHED_TRANS_RE = re.compile(
    r'<translation type="unfinished">([^<]*)</translation>',
    re.DOTALL,
)


def html_unescape(s: str) -> str:
    return (
        s.replace("&apos;", "'")
        .replace("&quot;", '"')
        .replace("&amp;", "&")
        .replace("&lt;", "<")
        .replace("&gt;", ">")
    )


def html_escape(s: str) -> str:
    return (
        s.replace("&", "&amp;")
        .replace("<", "&lt;")
        .replace(">", "&gt;")
        .replace('"', "&quot;")
        .replace("'", "&apos;")
    )


_HANGUL_RE = re.compile(r"[가-힣ㄱ-ㅎㅏ-ㅣ]")


def _is_passthrough(src: str) -> bool:
    """Strings that should be used as-is (no translation needed)."""
    # Single char or just a punctuation/symbol
    if len(src) <= 2 and not src.isalpha():
        return True
    # Already Korean? Pass through (developer wrote qsTr("Korean string") directly)
    if _HANGUL_RE.search(src):
        return True
    # Pure number / decimal / pct / time strings
    if re.fullmatch(r"[\d\.\-\+\:\s%°/]+", src):
        return True
    # Number + unit (Hz, ms, dBm, etc.)
    if re.fullmatch(
        r"\d+(\.\d+)?\s*"
        r"(Hz|kHz|MHz|GHz|ms|μs|us|ns|s|min|h|"
        r"dB|dBm|dBi|V|mV|kV|A|mA|W|kW|mW|"
        r"%|°|°C|°F|"
        r"m|cm|mm|km|ft|in|mi|nm|"
        r"m/s|km/h|mph|kt|fps|"
        r"bit|byte|B|KB|MB|GB|TB|"
        r"rad|rad/s|deg|deg/s|RPM|"
        r"px|pt|"
        r"g|kg|mg|lb|oz)?",
        src,
    ):
        return True
    # Single uppercase abbreviation (3-6 chars, all caps, possibly digits)
    if re.fullmatch(r"[A-Z][A-Z0-9]{1,6}", src):
        return True
    # Lowercase 2-4 letter technical abbreviation (pwm, rtk, rtl, gps, usb, etc.)
    if re.fullmatch(r"[a-z]{2,4}", src):
        return True
    # Single letter (X, Y, Z, L, H, etc.)
    if re.fullmatch(r"[A-Za-z]", src):
        return True
    # Mixed case short abbreviations (Vz, Vy, Rpm, etc.)
    if re.fullmatch(r"[A-Z][a-z][a-z]?", src):
        return True
    # Empty / whitespace
    if not src.strip():
        return True
    return False


def translate(source: str, existing_glossary: dict[str, str]) -> str | None:
    """Return Korean translation or None if no confident translation."""
    src = html_unescape(source).strip()

    # 0. Passthrough (use source unchanged)
    if _is_passthrough(src):
        return src

    # 1. Glossary match (from existing finished translations in same file)
    if src in existing_glossary:
        return existing_glossary[src]

    # 2. Curated TERMS exact match
    if src in TERMS:
        return TERMS[src]

    # 3. Pattern rules
    for regex, fn in PATTERN_RULES:
        m = regex.fullmatch(src)
        if m:
            return fn(m)

    return None


def build_glossary(ts_path: Path) -> dict[str, str]:
    """Build source->translation map from FINISHED translations in the file."""
    glossary: dict[str, str] = {}
    text = ts_path.read_text(encoding="utf-8")
    # Match finished translations: <translation>NOT_EMPTY</translation> (no type=)
    pattern = re.compile(
        r"<source>(.*?)</source>\s*"
        r"(?:<extracomment>.*?</extracomment>\s*)?"
        r"(?:<translatorcomment>.*?</translatorcomment>\s*)?"
        r"<translation>([^<][^<]*?)</translation>",
        re.DOTALL,
    )
    for m in pattern.finditer(text):
        src = html_unescape(m.group(1)).strip()
        tgt = html_unescape(m.group(2)).strip()
        if src and tgt and tgt != src:  # skip identical/empty
            # Only set if not already seen with different translation
            if src not in glossary:
                glossary[src] = tgt
    return glossary


def process_file(ts_path: Path) -> tuple[int, int, list[str]]:
    """Process one .ts file, return (translated, still_unfinished, unmatched_samples)."""
    text = ts_path.read_text(encoding="utf-8")
    glossary = build_glossary(ts_path)
    print(f"  Glossary size from {ts_path.name}: {len(glossary)}")

    translated = 0
    still_unfinished = 0
    unmatched: list[str] = []

    def replace_message(m: re.Match) -> str:
        nonlocal translated, still_unfinished
        body = m.group(1)
        unf = UNFINISHED_TRANS_RE.search(body)
        if not unf:
            return m.group(0)  # not unfinished, leave alone

        src_m = SOURCE_RE.search(body)
        if not src_m:
            return m.group(0)
        source_xml = src_m.group(1)

        kr = translate(source_xml, glossary)
        if kr is None:
            still_unfinished += 1
            unmatched.append(html_unescape(source_xml))
            return m.group(0)

        translated += 1
        new_unf = f"<translation>{html_escape(kr)}</translation>"
        new_body = body[: unf.start()] + new_unf + body[unf.end():]
        return m.group(0).replace(body, new_body, 1)

    new_text = MESSAGE_RE.sub(replace_message, text)
    if new_text != text:
        ts_path.write_text(new_text, encoding="utf-8")

    return translated, still_unfinished, unmatched


def main(argv: list[str]) -> int:
    if len(argv) < 2:
        print("usage: auto_translate_ko.py <ts_file> [<ts_file> ...]", file=sys.stderr)
        return 2

    for arg in argv[1:]:
        path = Path(arg)
        print(f"\n== {path} ==")
        if not path.exists():
            print(f"  ERROR: file not found", file=sys.stderr)
            continue
        translated, remaining, unmatched = process_file(path)
        print(f"  translated:      {translated}")
        print(f"  still unfinished:{remaining}")
        if unmatched:
            dump = path.with_suffix(".unmatched.txt")
            dump.write_text(
                "\n".join(sorted(set(unmatched), key=lambda s: (len(s), s))),
                encoding="utf-8",
            )
            print(f"  unmatched dumped to: {dump}")
    return 0


if __name__ == "__main__":
    sys.exit(main(sys.argv))
